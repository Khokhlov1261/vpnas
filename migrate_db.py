    PG_PORT = int(os.environ.get("PG_PORT", 5432))
    PG_DB = os.environ.get("PG_DB", "securelink")
    PG_USER = os.environ.get("PG_USER", "securelink")
    PG_PASSWORD = os.environ.get("PG_PASSWORD", "password")
    
